#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	if (n >= 0 && n <= 9)
		ft_putchar(n + 48);
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + 48);
	}
}
