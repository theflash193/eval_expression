#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	int	i;
	char	*tmp;

	tmp = s;
	i = 0;
	while (n > 0)
	{
		tmp[i] = (unsigned char)0;
		i++;
		n--;
	}
	return (s);
}
